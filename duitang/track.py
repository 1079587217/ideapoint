import random


def track(
        min_points=16,
        max_points=20,
        max_x=120,
        jitter_range=5,
        time_step=100
):
    """
    生成一个模拟滑块轨迹的二维数组，起始点为 [0, 0, 0]

    参数：
        min_points: 最小轨迹点数量
        max_points: 最大轨迹点数量
        max_x: 滑块终点 x 值
        jitter_range: y 方向上的抖动范围
        time_step: 每步时间间隔（毫秒）

    返回：
        [[x, y, t], ...] 格式的轨迹列表
    """
    num_points = random.randint(min_points, max_points)
    track = []
    current_x = 0
    total_time = 0

    # 起始点固定为 [0, 0, 0]
    track.append([0, random.randint(-5, 3), 0])

    for i in range(1, num_points):
        # 控制前期快、后期慢的移动速度
        if i < int(num_points * 0.6):
            step = random.randint(5, 10)
        else:
            step = random.randint(2, 8)

        current_x += step
        if current_x > max_x:
            current_x = max_x

        # y 值做小幅抖动
        jitter = random.randint(-jitter_range , jitter_range + 5)

        # 时间递增，加入一点随机性让行为更自然
        total_time += time_step + random.randint(0, 3)

        track.append([current_x, jitter, total_time])

    return track


